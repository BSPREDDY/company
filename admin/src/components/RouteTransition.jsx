import { motion } from 'framer-motion';
import { useLocation } from 'react-router-dom';

/**
 * Wrapper component for smooth route transitions
 * Provides fade and slide animations when navigating
 */
export const RouteTransition = ({ children }) => {
  const location = useLocation();

  const pageVariants = {
    initial: {
      opacity: 0,
      y: 20,
    },
    in: {
      opacity: 1,
      y: 0,
    },
    out: {
      opacity: 0,
      y: -20,
    },
  };

  const pageTransition = {
    type: 'tween',
    duration: 0.4,
    ease: 'easeInOut',
  };

  return (
    <motion.div
      key={location.pathname}
      initial="initial"
      animate="in"
      exit="out"
      variants={pageVariants}
      transition={pageTransition}
      className="w-full"
    >
      {children}
    </motion.div>
  );
};

